package de.uniwue.jpp.deckgen.model;

public class WrongDeckSizeException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private static String myMessage = "Nicht 10 Karten im Deck";
	
	public WrongDeckSizeException() {
		super(myMessage);
	}

	public WrongDeckSizeException(String message) {
		super(myMessage);
		
	}

	public WrongDeckSizeException(Throwable cause) {
		super(cause);
		
	}

	public WrongDeckSizeException(String message, Throwable cause) {
		super(myMessage, cause);
		
	}

	public WrongDeckSizeException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(myMessage, cause, enableSuppression, writableStackTrace);
		
	}

}
